"""
#10 Calculo de preço de pintura

Faça um algoritmo que leia a largura e altura de uma parede, calcule e
mostre a área a ser pintada e a quantidade de tinta necessária para o serviço,
sabendo que cada litro de tinta pinta uma área de 2metros quadrados.

1. Quais são os dados de entrada necessário?
Largura e Altura da parede

2. O que devo fazer com estes dados?
Calcular a área
Calcular quanto de tinha será necessária

3. Quais são as restrições deste problema?
Os números tem que ser positivos

4. Qual é o resultado esperado?
A multiplicação dos dois números (área)
Divisão da área por 2 (a cada litro de tinta pinta 2 metros quadrados)

5. Qual é a sequência de passos a ser feitas para chegar ao resultado?
Pedir ao usuário a altura da parede
Pedir ao usuário a largura da parede
multiplcar altura e largura
dividir a multiplicaçao por 2
retornar "a area a ser pintada é de X metros quadrados"
retornar "voce vai precisar de X litros de tinta para pintar essa área"
"""
altura = float(input("Qual a altura da sua parede?"))
largura = float(input("Qual a largura da sua parede?"))

area = altura * largura
tinta = area / 2

print(f"A area a ser pintada é de {area:.2f} metros quadrados")
print(f"Você vai precisar de {tinta:.2f} litros de tinta para pintar essa área")