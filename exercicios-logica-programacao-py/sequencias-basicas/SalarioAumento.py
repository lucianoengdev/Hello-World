"""
#13 Faça um algoritmo que leia o salário de um funcionário, calcule e mostre o seu novo salário, com 15% de aumento.

1. Quais são os dados de entrada necessário?
Salário do usuário

2. O que devo fazer com estes dados?
Calcular 115% do valor total

3. Quais são as restrições deste problema?
Tem de ser um número positivo por que é salário

4. Qual é o resultado esperado?
1,15X

5. Qual é a sequência de passos a ser feitas para chegar ao resultado?
Pedir ao usuário o valor do seu salário
multiplicar o preço por 1,15
mostrar o resultado ao usuário
"""

salario = float(input("Quanto você ganha por mês?"))
ajustado = salario * 1.15

print(f"Com o aumento de 15% seu novo salário é ${ajustado:.2f}")